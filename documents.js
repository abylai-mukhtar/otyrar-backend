(function initDocumentsFromCms() {
  const LOCAL_KEY = 'otyrar_documents';

  function isArray(val) {
    return Array.isArray(val);
  }

  function applyDocuments(docs) {
    if (!isArray(docs) || !docs.length) return;
    window.MUSEUM_DOCUMENTS = docs;
    localStorage.setItem(LOCAL_KEY, JSON.stringify(docs));
    window.dispatchEvent(new Event('cms:documents-updated'));
  }

  try {
    const localRaw = localStorage.getItem(LOCAL_KEY);
    if (localRaw) {
      const localDocs = JSON.parse(localRaw);
      if (isArray(localDocs) && localDocs.length) {
        window.MUSEUM_DOCUMENTS = localDocs;
      }
    }
  } catch {
    // Keep static fallback.
  }

  fetch('/api/public/content')
    .then((res) => (res.ok ? res.json() : null))
    .then((payload) => {
      const docs = payload?.content?.otyrar_documents;
      applyDocuments(docs);
    })
    .catch(() => {
      // Keep current documents set on fetch errors.
    });
})();
